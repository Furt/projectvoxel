package me.furt.platformer.character;

/**
 *
 * @author Terry
 */
public class Stats {
    public int strength;
    public int agility;
    public int stamina;
    
    public void setStrength(int i) {
        this.strength = i;
    }
    
    public int getStrength() {
        return this.strength;
    }
    
    public void setAgility(int i) {
        this.agility = i;
    }
    
    public int getAgility() {
        return this.agility;
    }
    
    public void setStamina(int i) {
        this.stamina = i;
    }
    
    public int getStamina() {
        return this.stamina;
    }
    
}
